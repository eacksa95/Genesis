    private void fillView(Map<String, String> data) {
        for (Map.Entry<String, String> entry : data.entrySet()) {
            String key = entry.getKey();
            String value = entry.getValue();
            switch (key) {
                case "id":
                    tf_id.setText(value);
                    break;
                case "nombre":
                    tf_nombre.setText(value);
                    break;

            }//end switch
        }//end for
    }//end fill

